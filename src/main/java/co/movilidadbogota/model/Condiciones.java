package co.movilidadbogota.model;

public class Condiciones {

	String plazoEjecucion;
	String tipoPlazoEjecucion;
	String observaciones;
	String valorMensual;
	String valordisp;
	String calculo;
	String soporteCalculo;
	String tipoPropuesta;
	String formaPago;
	String sitioEntrega;

	public String getPlazoEjecucion() {
		return plazoEjecucion;
	}

	public void setPlazoEjecucion(String plazoEjecucion) {
		this.plazoEjecucion = plazoEjecucion;
	}

	public String getTipoPlazoEjecucion() {
		return tipoPlazoEjecucion;
	}

	public void setTipoPlazoEjecucion(String tipoPlazoEjecucion) {
		this.tipoPlazoEjecucion = tipoPlazoEjecucion;
	}

	public String getObservaciones() {
		return observaciones;
	}

	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}

	public String getValorMensual() {
		return valorMensual;
	}

	public void setValorMensual(String valorMensual) {
		this.valorMensual = valorMensual;
	}

	public String getValordisp() {
		return valordisp;
	}

	public void setValordisp(String valordisp) {
		this.valordisp = valordisp;
	}

	public String getCalculo() {
		return calculo;
	}

	public void setCalculo(String calculo) {
		this.calculo = calculo;
	}

	public String getSoporteCalculo() {
		return soporteCalculo;
	}

	public void setSoporteCalculo(String soporteCalculo) {
		this.soporteCalculo = soporteCalculo;
	}

	public String getTipoPropuesta() {
		return tipoPropuesta;
	}

	public void setTipoPropuesta(String tipoPropuesta) {
		this.tipoPropuesta = tipoPropuesta;
	}

	public String getFormaPago() {
		return formaPago;
	}

	public void setFormaPago(String formaPago) {
		this.formaPago = formaPago;
	}

	public String getSitioEntrega() {
		return sitioEntrega;
	}

	public void setSitioEntrega(String sitioEntrega) {
		this.sitioEntrega = sitioEntrega;
	}

}
